/**
 * GUI applications are event-driven. An application reacts to different event types which are generated
 * during its lifetime. Events are generated by a user (a mouse click), an application (a timer), or the system(a clock).
 *
 * An event is a notification about a change. it encapsulates the state changes in the event source. Registered
 * event filters and event handlers within the application receive the event and provide a response.
 *
 * Every event in JavaFX has three properties:
 * - Event source
 * - Event target
 * - Event type
 *
 * Event source is the object whose state changes; it generates events. Event target is the destination of the an
 * event. Event type provides additional classification to events of the same Event class.
 *
 * Event source object delegates the task of handling an event to the event handler. When the event occurs, the
 * event source creates an event object and sends it to each registered handler.
 */
