package jgui.javafx;

import javafx.application.Application;
import javafx.stage.Stage;

/**
 * The launch() method detects that it is called from the run() method of the MyJavaFXApp$1 class.
 * MyJavaFXApp$1 class ia an enonymous inner class generated by the compiler, which is a subclas of the Object class,
 * not the Application class, and it implements the Runnable interface.
 *
 * @author JiaweiMao
 * @version 1.0.0
 * @date 2016.12.19, 3:49 PM
 */
public class MyJavaFXApp extends Application {

    public static void main(String[] args) {
        Thread t = new Thread(new Runnable() {
            @Override public void run() {
                Application.launch(args);
            }
        });
        t.start();
    }

    @Override public void start(Stage primaryStage) throws Exception {

    }
}
